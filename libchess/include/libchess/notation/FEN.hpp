/** @defgroup notation Notation
    Functions for working with various types of chess game notation.
 */

/** @file
    This file provides functions for converting Position objects to
    and from FEN strings.

    @ingroup notation
 */

#pragma once

#include <libchess/game/Position.hpp>
#include <string>
#include <string_view>

/** This namespace contains functions for working with various types of
    chess game notation.

    @ingroup notation
 */
namespace chess::notation {

using game::Position;

/** Returns the FEN representation of the given position.

    @ingroup notation
    @see from_fen()
 */
[[nodiscard]] std::string to_fen(const Position& position);

/** Returns a Position object encoding the given FEN string.

    @throws std::invalid_argument An exception will be thrown if the
    FEN string cannot be parsed correctly.

    @ingroup notation
    @see to_fen()
 */
[[nodiscard]] Position from_fen(std::string_view fenString);

} // namespace chess::notation
